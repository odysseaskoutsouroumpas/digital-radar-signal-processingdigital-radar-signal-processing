import numpy as np

# Ορισμός πινάκων για ένα γραμμικό σύστημα
A = np.array([[1, 2, 3], [4, 5, 6], [7, 8, 10]])
b = np.array([1, 2, 3])

# Πολλαπλασιασμός πίνακα με σταθερά
C1 = 3 * A
print(C1)

# Δύναμη στοιχείου προς στοιχείο
C2 = A**2
print(C2)

# Πολλαπλασιασμός πινάκων
C3 = np.dot(A, A)
print(C3)

# Πολλαπλασιασμός πίνακα με διάνυσμα
C4 = np.dot(A, b)
print(C4)

# Επίλυση γραμμικού συστήματος
C5 = np.linalg.solve(A, b)
print(C5)
